define(function(require, exports, module) {
	var BaseView=require("baseview");
	
	var Community  = BaseView.extend({
		initSubnav:function(){
			return {
				model:{
					title:"社区信息维护"
				}
			};
		},
		initList:function(widget){
			return {
				url : "api/community/query",
				params:{
					fetchProperties:"*,organization.name"
				},
				model:{
					columns:[{
						key:"name",
						name:"名称",
						format:"detail",
						formatparams:[{
							key:"detail",
							handler:function(index,data,rowEle){
								widget.edit("detail",data);
								return false;
							}
						}]
					},{
						key:"code",
						name:"编码"
					},{
						key:"zipCode",
						name:"邮编"
					},{
						key:"address",
						name:"地址"
					},{
						key:"organization.name",
						name:"所属机构"
					},{
						key:"membersLimit",
						name:"会员上限"
					},{
						key:"description",
						name:"备注"
					},{
						key:"operate",
						name:"操作",
						format:"button",
						formatparams:[{
							key:"edit",
							icon:"edit",
							handler:function(index,data,rowEle){
								widget.edit("edit",data);
								return false;
							}
						},{
							key:"delete",
							icon:"remove",
							handler:function(index,data,rowEle){
								widget.del("api/community/" + data.pkCommunity + "/delete");
							}
						}]
					}]
				}
			};
		},
		initCard:function(widget){
			return {
				compType:"profile",
				saveaction:function(){
					widget.save("api/community/add",$("#community").serialize());
				},
				model:{
					id:"community",
					items:[{
						title:"社区信息",
						icon:"github",
						remark:"",
						children:[{
							name:"pkCommunity",
							type:"hidden"
						},{
							name:"version",
							type:"hidden",
							defaultValue:"0"
						},{
							label:"编码",
							name:"code",
							validate:["required"]
						},{
							label:"名称",
							name:"name",
							validate:["required"]
						},{
							label:"邮编",
							name:"zipCode",
							validate:["required","postalcode"]
						},{
							label:"地址",
							name:"address",
							type:"textarea",
							validate:["required"]
						},{
							label:"所属机构",
							name:"organization",
							type:"select",
							url:"api/organization/query",
							key:"pkOrganization",
							value:"name",
							validate:["required"]
						},{
							label:"会员上限",
							name:"membersLimit",
							validate:["required","number"]
						},{
							name:"description",
							label:"备注",
							type:"textarea",
						}]
					},{
						title:"联系人1（必填）",
						icon:"user",
						remark:"",
						children:[{
							label:"姓名",
							name:"contact1Name",
							validate:["required"]
						},{
							label:"联系电话",
							name:"contact1Phone",
							validate:["required"]
						},{
							label:"E-mail",
							name:"contact1Email",
							validate:["required","email"]
						},{
							label:"邮编",
							name:"contact1ZipCode",
							validate:["required","postalcode"]
						},{
							label:"地址",
							name:"contact1Address",
							type:"textarea",
							validate:["required"]
						}]
					},{
						title:"联系人2（非必填）",
						icon:"user",
						remark:"",
						children:[{
							label:"姓名",
							name:"contact2Name"
						},{
							label:"联系电话",
							name:"contact2Phone"
						},{
							label:"E-mail",
							name:"contact2Email",
							validate:["email"]
						},{
							label:"邮编",
							name:"contact2ZipCode",
							validate:["postalcode"]
						},{
							label:"地址",
							name:"contact2Address",
							type:"textarea"
						}]
					}]
					
				}
			};
		}
	});
	module.exports = Community;
});