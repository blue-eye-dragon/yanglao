define(function(require,exports,module){
	var Form=require("form-2.0.0")
	
	var init=function(){
		var list=[{
			key:"true",
			value:"是"
		},{
			key:"false",
			value:"否",
			isDefault:true
		}];
		var form=new Form({
			parentNode:"#ciservice",
			model:{
				id:"ciserviceform",
				defaultButton:false,
				items:[{
					name:"checkInTelecom.pkCITelecom",
					type:"hidden"
				},{
					name:"checkInTelecom.version",
					type:"hidden"
				},{
					name:"checkInTelecom.openType",
					label:"开通方式",
					type:"radiolist",
					list:[{
						key:"Agency",
						value:"代理开通"
					},{
						key:"Self",
						value:"自行开通",
						isDefault:true
					}],
				},{
					name:"checkInTelecom.telePhoneOpened",
					label:"电话",
					type:"radiolist",
					list:list
				},{
					name:"checkInTelecom.phoneNumber",
					label:"电话号码"
				},{
					name:"checkInTelecom.ipTVOpened",
					label:"IPTV",
					type:"radiolist",
					list:list
				},{
					name:"checkInTelecom.broadbandOpened",
					label:"宽带",
					type:"radiolist",
					list:list
				},{
					name:"checkInNaturalGas.version",
					type:"hidden"
				},{
					name:"checkInNaturalGas.pkCINaturalGas",
					type:"hidden"
				},{
					name:"checkInNaturalGas.naturalGas",
					label:"天然气",
					type:"radiolist",
					list:list
				},{
					name:"checkInMembershipCard.pkCIMembershipCard",
					type:"hidden"
				},{
					name:"checkInMembershipCard.version",
					type:"hidden"
				},{
					name:"checkInMembershipCard.cardCurrency",
					label:"一卡通",
					type:"radiolist",
					list:list
				},{
					name:"checkInMembershipCard.cardCurrencyMoney",
					label:"预充金额"
				},{
					name:"checkInMoveHouse.pkCIMovehouse",
					type:"hidden"
				},{
					name:"checkInMoveHouse.version",
					type:"hidden"
				},{
					name:"checkInMoveHouse.sendCar",
					label:"是否派车",
					type:"radiolist",
					list:list,
					defaultValue:"false",
				},{
					name:"checkInMoveHouse.address",
					label:"派车地址",
					validate:["required"]
				},{
					name:"checkInMoveHouse.moveHouseDate",
					type:"date",
					mode:"Y-m-d H:i",
					label:"派车时间",
					validate:["required"]
				}]
			}
		});
		seajs.on("el-event-checkinimplement-edit",function(data){
			if(data.checkInTelecom&&data.checkInTelecom.openType.key=="Agency"){
				form.show(["checkInTelecom.telePhoneOpened","checkInTelecom.ipTVOpened","checkInTelecom.broadbandOpened","checkInTelecom.phoneNumber"]);
			}else{
				form.hide(["checkInTelecom.telePhoneOpened","checkInTelecom.ipTVOpened","checkInTelecom.broadbandOpened","checkInTelecom.phoneNumber"]);
			}
			if(data.checkInMoveHouse&&data.checkInMoveHouse.sendCar){
				//data.checkInMoveHouse.sendCar=true;
				form.show(["checkInMoveHouse.address","checkInMoveHouse.moveHouseDate"]);
			}else{
				form.hide(["checkInMoveHouse.address","checkInMoveHouse.moveHouseDate"]);
			}
			form.setData(data || {});
		});
		return form;
	};
	module.exports=init;
});